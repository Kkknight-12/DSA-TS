/**
 * Celebrity Problem - TWO POINTERS SOLUTION (Most Optimal)
 *
 * Approach: Use two pointers to eliminate non-celebrities
 * Time Complexity: O(n)
 * Space Complexity: O(1) - No extra space!
 *
 * KEY INSIGHT:
 * Same elimination logic as Stack, but without using extra space!
 *
 * Two pointers: left = 0, right = n-1
 * - If left knows right â†’ left is NOT celebrity â†’ left++
 * - If left doesn't know right â†’ right is NOT celebrity â†’ right--
 * - When left == right â†’ we have our CANDIDATE
 *
 * Why this works?
 * - Har comparison mein ek pointer move hota hai
 * - Jo pointer move hua, wo ELIMINATE ho gaya
 * - n-1 moves mein n-1 log eliminate â†’ 1 CANDIDATE
 */

