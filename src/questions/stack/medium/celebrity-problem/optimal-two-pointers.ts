    /**
     * Main function - Find the celebrity using Two Pointers
     *
     * @param M - NÃ—N matrix where M[i][j] = 1 means i knows j
     * @returns Celebrity's index or -1 if no celebrity
     */
    export function findCelebrity(M: number[][]): number {
        const n = M.length;

        // Edge Case: Less than 2 people
        if (n < 2) return -1;

        // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
        // PHASE 1: Find Candidate using Two Pointers
        // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
        // WHY two pointers? Same elimination as stack, but O(1) space!
        //
        // left starts from beginning (0)
        // right starts from end (n-1)
        // They move towards each other, eliminating one person per step

        let left = 0;
        let right = n - 1;

        // Keep eliminating until only one person remains
        // WHY left < right? Jab tak dono alag hain, compare karo
        while (left < right) {
            // Compare: Does left know right?
            if (M[left][right] === 1) {
                // left knows right
                // â†’ left jaanta hai kisi ko
                // â†’ left celebrity NAHI ho sakta
                // â†’ Eliminate left, move to next person
                left++;
            } else {
                // left does NOT know right
                // â†’ Koi hai (left) jo right ko nahi jaanta
                // â†’ right celebrity NAHI ho sakta
                // â†’ Eliminate right, move to previous person
                right--;
            }
        }

        // After n-1 eliminations, left == right
        // This is our CANDIDATE (not guaranteed celebrity yet!)
        const candidate = left; // or right, they're same now

        // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
        // PHASE 2: Verification - Is candidate really a celebrity?
        // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
        // Same verification as Stack approach
        //
        // WHY verify? Elimination sirf candidate deta hai, guarantee nahi
        //
        // We need to verify BOTH conditions:
        // 1. Candidate knows NO ONE (row check)
        // 2. EVERYONE knows candidate (column check)

        for (let i = 0; i < n; i++) {
            // Skip self comparison
            if (i === candidate) continue;

            // CHECK 1: Candidate should NOT know anyone
            // If candidate knows person i, candidate is NOT celebrity
            if (M[candidate][i] === 1) {
                return -1; // Failed! Candidate knows someone
            }

            // CHECK 2: Everyone should know candidate
            // If person i doesn't know candidate, candidate is NOT celebrity
            if (M[i][candidate] === 0) {
                return -1; // Failed! Someone doesn't know candidate
            }
        }

        // Both checks passed! Candidate is the CELEBRITY!
        return candidate;
    }

